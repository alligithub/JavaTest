public class EvenAndOddNumber {
    //четное или не четное число
    public static void main(String args[]) {
        int n = 53;
        System.out.print("Число " + n + " ");
        if( n % 2 == 0) {
            System.out.println("четное");
        } else {
            System.out.println("нечетное");
        }
    }
}
